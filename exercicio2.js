let cor = "esmeralda"; // Em inglês

switch(cor) {
  case "red": 
    console.log("Vermelho");
    break;
  case "green":
    console.log("Verde");
    break;
  case "blue":
    console.log("Azul");
    break;
  case "yellow": 
    console.log("Amarelo");
    break;
  case "black":
    console.log("Preto");
    break;
  case "white":
    console.log("Branco");
    break;
  default:
    console.log("Cor não registrada");
    break;
}